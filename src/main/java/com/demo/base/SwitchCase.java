package com.demo.base;

public class SwitchCase {
	
	public static void main(String[] args) {
		
		String day = "Sunday";
		
		switch(day) {
		
		case "Sunday": 
			System.out.println(1);
			break;
			
		case "Monday":
			System.out.println(2);
			break;
			
		case "Tuesday":
			System.out.println(3);
			break;
		}
	}

}
